import { DATE_FORMATS } from '../constants';

// Date formatting utilities (replacing jQuery date formatting)
export const formatDate = (date: string | Date, format: string = DATE_FORMATS.DISPLAY): string => {
  const d = new Date(date);
  
  if (isNaN(d.getTime())) {
    return 'Invalid Date';
  }

  const months = [
    'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
    'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'
  ];

  const monthsFull = [
    'January', 'February', 'March', 'April', 'May', 'June',
    'July', 'August', 'September', 'October', 'November', 'December'
  ];

  const padZero = (num: number): string => num.toString().padStart(2, '0');

  switch (format) {
    case DATE_FORMATS.DISPLAY:
      return `${months[d.getMonth()]} ${padZero(d.getDate())}, ${d.getFullYear()}`;
    
    case DATE_FORMATS.DISPLAY_WITH_TIME:
      return `${months[d.getMonth()]} ${padZero(d.getDate())}, ${d.getFullYear()} ${padZero(d.getHours())}:${padZero(d.getMinutes())}`;
    
    case DATE_FORMATS.INPUT:
      return `${d.getFullYear()}-${padZero(d.getMonth() + 1)}-${padZero(d.getDate())}`;
    
    case DATE_FORMATS.API:
      return `${d.getFullYear()}-${padZero(d.getMonth() + 1)}-${padZero(d.getDate())} ${padZero(d.getHours())}:${padZero(d.getMinutes())}:${padZero(d.getSeconds())}`;
    
    default:
      return d.toLocaleDateString();
  }
};

